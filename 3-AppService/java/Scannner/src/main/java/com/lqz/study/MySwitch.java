public class MySwitch {

    public static void main(String[] args) {
        int number = 0;
        switch (number)
        {
            case 0 :
                System.out.println("0");
                break;
            case 1:
                System.out.println("1");
                break;
            case 2:
                System.out.println("2");
                break;
            default:
                System.out.println("other");
                break;
        }

    }
}
